package com.project.unibibliotek.model;

import java.net.URL;

public class WebServiceParameters {
	
	private URL url;
	
	private String scope;

	public URL getUrl() {
		return url;
	}

	public void setUrl(URL url) {
		this.url = url;
	}

	public String getScope() {
		return scope;
	}

	public void setScope(String scope) {
		this.scope = scope;
	}
	
	
	
}
